import UIKit

extension UIFont {
    
    ///MessageCell中label的字体大小
    public class func fontTextMessageText() -> UIFont {
        return UIFont.systemFont(ofSize: 16)
    }
    
    public class func fontFriendsUsername() -> UIFont {
        return UIFont.systemFont(ofSize: 17)
    }
    
    
}
